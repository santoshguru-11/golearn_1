package main

import "fmt"

func main() {
	// Declaring variables
	var x, y bool
	x = true
	y = false

	// Logical operators
	fmt.Println("Logical Operators:")
	fmt.Println("x && y:", x && y) // Logical AND
	fmt.Println("x || y:", x || y) // Logical OR
	fmt.Println("!x:", !x)         // Logical NOT
}
